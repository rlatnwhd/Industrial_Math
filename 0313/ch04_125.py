import math

print("3의 제곱근 =",math.sqrt(3)) # sqrt (제곱근)
print("10의 지수함수 =",math.exp(10)) # exp (지수함수)
print("1의 자연로그값 =",math.log(1)) # log (로그)
print("파이의 코사인 값 =",math.cos(math.pi * 0)) # cos (코사인)
print("파이의 사인값 =",math.sin(math.pi * 3 / 2)) # sin (사인)
print("파이의 탄젠트값 =",math.tan(math.pi / 4)) # tan (탄젠트)