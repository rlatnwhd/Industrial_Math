# 산술 연산자를 이용한 다양한 연산
a = 7
b = 4

print("덧셈 :",a + b) # 것셈
print("뺼셈 :",a - b) # 뺼셈
print("곱셈 :",a * b) # 곱셈
print("나눗셈 :",a / b) # 나눗셈
